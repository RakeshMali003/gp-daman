<div class="entry clr" itemprop="text">
<p>The Administrator of Daman &amp; Diu and Dadra &amp; Nagar Haveli hereby makes the following “ The Government Polytechnic, Daman Hostel Rules, 1997”:</p>
<ol>
<li><strong><u>SHORT TITLE AND COMMENCEMENT :</u></strong></li>
<li>These rules may be called the Government Polytechnic, Daman Hostel Rules. 1997.</li>
<li>They shall come into force at once.</li>
<li><strong><u>ADMISSION :</u></strong></li>
<li>Admission to The Government Polytechnic hostel shall be restricted to bonafide students of the government polytechnic and shall be strictly on the basis of these rules.</li>
<li>Admission to the hostel will be for the period of one academic term only.</li>
</ol>
<ul>
<li>Application for hostel accommodation shall be submitted in the prescribed form available in the office of the Principal / Warden, anytime after the commencement of each academic term.</li>
</ul>
<ol>
<li>Accommodation shall be provided on the basis of availability and need. Outstation students will be given first preference.</li>
<li>Admission to the hostel and allocation of rooms is left at the discretion of the Principal / Warden / Superintendent of hostel whose decision in this regard shall be final. No student shall be admitted to the hostel unless the requisite fees and caution money deposit are paid.</li>
<li>Renewal of admission to hostel and transfer of rooms shall be done at the beginning of every term including compulsory transfer as per reservations of hostel. Mutual transfer between hostelites is not permissible under these rules.</li>
</ol>
<ul>
<li>Students must vacate hostel accommodation every semester immediately after semester examination for maintenance work by P.W.D. room possession should be given to the Hostel Clerk and receipt obtained to that effect. Re-allotment in fresh semester will be done if a student had vacated the allotment in prior semester in time.</li>
</ul>
<ol start="3">
<li><strong><u>MAINTENANCE OF ROOMS :</u></strong></li>
<li>Musical Instruments, Radios, Tvs ,Video etc. shall not be allowed in the rooms.</li>
<li>The authorities are not responsible for loss of jewellery, cash etc. from the rooms. The hostelites in their own interest are advised not to keep any valuable in the rooms.</li>
</ol>
<ul>
<li>Use of electrical appliances is not permitted.</li>
</ul>
<ol>
<li>The hostelites shall be held responsible for damages to the building, fixtures and furniture in the hostel as a result of their activity. They will be penalised for damages individually or collectively depending upon the situation.</li>
<li>Fan and lights in the rooms may be used only when required and should be switched off every time they leave their rooms.</li>
<li>The maintenance of peace shall be collective responsibility of the students in the hostel. Any disturbance in the hostel of any kind may lead to disciplinary action.</li>
</ol>
<ul>
<li>Gambling, possessing and consuming of alcohol and narcotic drugs shall be strictly prohibited in hostel premises.</li>
<li>No hostelite shall be allowed to keep any fire arms or lethal weapons in the hostel</li>
</ul>
<ol>
<li>All students residing in the hostel are accountable to the Principal, Warden, Hostel Superintendent for their orderly and good conduct. Activities other than those permitted by the Principal/ Hostel Superintendent are not allowed in the hostel. No political meetings shall be allowed in the hostel premises in any cases.</li>
<li>Hostelites shall be in the hostel campus by 9:30 p.m. prior permission shall be taken from the warden to stay away from the hostel beyond 9:30 p.m.</li>
<li>Each prefect will maintain an Attendance Register. A separated Register will be maintained with prefect / Hostel Superintendent in which a resident will sign before going home and after coming back home. After 9:30 p.m., residents are expected to remain in their room. At 10:00 p.m. all the gates will be locked by the watchman.</li>
</ol>
<ul>
<li>No hostelite shall stay overnight outside the Hostel without written intimation to the Warden. The hostelites shall record the overnight stay outside the hostel in the register maintained in the warden’s office.</li>
<li>The hostel authorities reserve the right to cancel the accommodation if a hostelite is found using the room infrequently.</li>
</ul>
<ol start="4">
<li><strong><u>VISITORS TO THE HOSTEL:</u></strong></li>
<li>The visitors will be allowed in the visitor’s room only. However, a male visitor may enter the room in the hostel provided he is escorted by the hostelite in and out of the hostel.</li>
<li>No female visitor will be allowed to enter the rooms in the hostel.</li>
</ol>
<ul>
<li>The authorities and the hostel staff are authorised to enter the room whenever the situation demands.</li>
</ul>
<ol>
<li>No guest will be allowed overnight stay in the hostel. The Parent / Guardian of the hostelites will be provided accommodation in the Guest House on payment, depending upon the availability.</li>
<li>All non-residents must leave the Hostel premises before 7:00 p.m. Outsiders are not allowed to stay in the resident room except for social visits during the day time and under no circumstances after 7:00 p.m.</li>
<li><strong><u>SICKNESS :</u></strong></li>
</ol>
<p>Every hostelite shall report to the Warden for any sickness. The Warden shall help the hostelite in getting proper medical care from the nearby Govt. Hospital / Health Centre and if need be, parents / Guardians of the hostelite shall be informed.</p>
<ol start="6">
<li><strong><u>FEES :</u></strong></li>
</ol>
<p>The hostelites must pay the fees on admission. Failing to pay the fees may lead to cancellation of Hostel admission.</p>
<p>The hostel caution money deposit (refundable) will be Rs.500/- and the hostel fees will be Rs.500/- per each term which will have to be paid at the beginning of the term.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<ol start="7">
<li><strong><u>HOSTEL MESS :</u></strong></li>
<li>It is compulsory for all the hostelites to join the mess in the hostel.</li>
<li>All the hostelites shall pay the mess deposit as per the rules and regulations of the mess.</li>
</ol>
<ul>
<li>The monthly mess established dues shall be paid within seven days of the notice on the board.</li>
</ul>
<ol>
<li>No credit in any case shall be permitted in the mess.</li>
<li><strong><u>VACATING THE ROOMS :</u></strong></li>
</ol>
<p>While vacating the room the hostelite shall pay all the dues of the hostel. They shall hand-over the charge of the room after proper inventory to the Housekeeper and shall obtain a ‘No Dues Certificate’ from the Warden. Failing which action may be taken as deemed fit by the authorities.</p>
<ol start="9">
<li><strong><u>RAGGING :</u></strong></li>
</ol>
<p>Ragging in any form is strictly prohibited. Anyone indulging in ragging shall be expelled from the Hostel / Government Polytechnic, Daman.</p>
<ol start="10">
<li><strong><u>DISCIPLINARY ACTION :</u></strong></li>
<li>The hostelites shall strictly abide by these Rules and Regulations relating to the Hostel, Mess, Reading Room / TV Room etc. The Warden is authorised to debar a hostelite from staying in the hostel for a period of one month if he violates the rules and regulations of hostel.</li>
<li>The Principal reserves the right to suspend or terminate the admission to the Hostel / Government Polytechnic, Daman after proper inquiry. The decision of the Principal shall be final.</li>
</ol>
<ul>
<li>The rules shall be subject to additions and alterations as deemed necessary.</li>
</ul>
